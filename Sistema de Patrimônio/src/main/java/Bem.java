public class Bem {
    private String name;
    private TipoBem tipo;

    // Getter para name
    public String getName() {
        return name;
    }

    // Setter para name
    public void setName(String name) {
        this.name = name;
    }

    // Getter para tipo
    public TipoBem getTipo() {
        return tipo;
    }

    // Setter para tipo
    public void setTipo(TipoBem tipo) {
        this.tipo = tipo;
    }
}
